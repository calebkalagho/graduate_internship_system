<script>
    // Validate phone number to allow only digits
    document.getElementById("mobile").addEventListener("input", function(e) {
        var mobile = e.target.value;
        var mobilePattern = /^[0-9]*$/;
        if (!mobilePattern.test(mobile)) {
            document.getElementById("mobile_error").textContent = "Only numbers are allowed.";
            e.target.value = mobile.replace(/[^0-9]/g, ''); // Remove non-digits
        } else {
            document.getElementById("mobile_error").textContent = "";
        }
    });

    // Validate password as the user types
    document.getElementById("password").addEventListener("input", function(e) {
        var password = e.target.value;
        if (password.length < 8) {
            document.getElementById("password_error").textContent = "Password must be at least 8 characters long.";
        } else {
            document.getElementById("password_error").textContent = "";
        }
    });

    // Validate confirm password as the user types
    document.getElementById("confirm_password").addEventListener("input", function(e) {
        var password = document.getElementById("password").value;
        var confirmPassword = e.target.value;
        if (confirmPassword !== password) {
            document.getElementById("confirm_password_error").textContent = "Passwords do not match.";
        } else {
            document.getElementById("confirm_password_error").textContent = "";
        }
        enableSubmitButton();
    });

    // Function to enable/disable submit button
    function enableSubmitButton() {
        var password = document.getElementById("password").value;
        var confirmPassword = document.getElementById("confirm_password").value;
        var passwordError = document.getElementById("password_error").textContent;
        var confirmPasswordError = document.getElementById("confirm_password_error").textContent;

        if (password && confirmPassword && password === confirmPassword && passwordError === "" && confirmPasswordError === "") {
            document.getElementById("submit_button").disabled = false;
        } else {
            document.getElementById("submit_button").disabled = true;
        }
    }

    // Validate National ID: Minimum 8 characters and live uniqueness check
    document.getElementById("national_id").addEventListener("input", function(e) {
        var nationalId = e.target.value;
        if (nationalId.length < 8) {
            document.getElementById("national_id_error").textContent = "National ID must be at least 8 characters.";
        } else {
            document.getElementById("national_id_error").textContent = "";
            checkNationalIdUnique(nationalId); // Call the AJAX function to check uniqueness
        }
    });

    // AJAX request to check if the National ID is unique
    function checkNationalIdUnique(nationalId) {
        var xhr = new XMLHttpRequest();
        xhr.open("POST", "./endpoint/check_national_id.php", true);
        xhr.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");
        xhr.onreadystatechange = function() {
            if (xhr.readyState === XMLHttpRequest.DONE) {
                var response = JSON.parse(xhr.responseText);
                if (response.exists) {
                    document.getElementById("national_id_error").textContent = "This National ID is already registered.";
                } else {
                    document.getElementById("national_id_error").textContent = "";
                }
                enableSubmitButton(); // Update submit button state after checking
            }
        };
        xhr.send("national_id=" + encodeURIComponent(nationalId));
    }

    // Enable/Disable submit button based on national_id and password errors
    function enableSubmitButton() {
        var nationalIdError = document.getElementById("national_id_error").textContent;
        var passwordError = document.getElementById("password_error").textContent;
        var confirmPasswordError = document.getElementById("confirm_password_error").textContent;

        if (!nationalIdError && !passwordError && !confirmPasswordError) {
            document.getElementById("submit_button").disabled = false;
        } else {
            document.getElementById("submit_button").disabled = true;
        }
    }
</script>

